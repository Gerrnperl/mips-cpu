main:
addi $0, $1, 4    # $0 = $1 + 4 = 0 + 4 = 4
subi $0, $0, 1    # $0 = $0 - 1 = 4 - 1 = 3
andi $1, $0, 0x0001 # $1 = $0 & 0x0001 = 3 & 0x0001 = 1
ori $2, $1, 0x0001 # $2 = $1 | 0x0001 = 1 | 0x0001 = 1
xori $3, $2, 0x0001 # $3 = $2 ^ 0x0001 = 1 ^ 0x0001 = 0
lui $4, 0x0001     # $4 = 0x0001 << 16 = 0x00010000
sw $0, 0($4)      # Memory[$4 + 0] = $0 = Memory[0x00010000] = 3
lw $5, 0($4)      # $5 = Memory[$4 + 0] = Memory[0x00010000] = 3
beq $5, $0, l1    # if ($5 == $0) goto l1 (3 == 3, 跳转到 l1)
addi $0, $0, 1    # 不执行，因为跳转到 l1
l1:
subi $0, $0, 1    # $0 = $0 - 1 = 3 - 1 = 2
bne $5, $0, l2    # if ($5 != $0) goto l2 (3 != 2, 跳转到 l2)
addi $0, $0, 1    # 不执行，因为跳转到 l2
l2:
slti $6, $0, 5    # $6 = ($0 < 5) ? 1 : 0 = (2 < 5) ? 1 : 0 = 1
sltiu $7, $0, 5   # $7 = ($0 < 5) ? 1 : 0 (unsigned) = (2 < 5) ? 1 : 0 = 1